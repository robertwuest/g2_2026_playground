class Person:
    def __init__(
        self,
        vorname,
        nachname,
        geburtstag,
        email,
        telefon,
        strassenname,
        strassennummer,
        plz,
        ort,
    ):
        self.vorname = vorname
        self.nachname = nachname
        self.geburtstag = geburtstag
        self.email = email
        self.telefon = telefon
        self.strassenname = strassenname
        self.strassennummer = strassennummer
        self.plz = plz
        self.ort = ort
 
