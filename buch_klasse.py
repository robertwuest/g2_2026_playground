class Buch:
    def __init__(self, titel, autor, erscheinungsjahr, isbn, seitenanzahl):
        """Initialisiert ein neues Buch-Objekt."""
        self.titel = titel
        self.autor = autor
        self.erscheinungsjahr = erscheinungsjahr
        self.isbn = isbn
        self.seitenanzahl = seitenanzahl